//Faça um programa que solicite o nome do usuário e imprima-o na vertical.
#include <stdio.h>
#include <string.h>

int main(void) {
  char nome[31];
  int i=0;
  
  printf("\n Escreva seu nome: ");
  scanf("%s", nome);

  while(nome[i] != '\0'){
    printf("%c\n", nome[i]);
    i++;
  }

    
  return 0;
}
